class LogicalOperation{
	public static void main(String[] args){
		boolean a = true;
		boolean b = false;

		//logical AND
		System.out.println("a && b = " + (a && b));

		//logical OR
		 System.out.println("a || b = " + (a || b));

		 //logical NOT
		  System.out.println("!a = " + (!a));
		   System.out.println("!b = " + (!b));
	}
}

